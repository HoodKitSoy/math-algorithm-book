import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
        // 1. 入力処理
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        sc.close();

        // 2. 出力処理
        int sum = 5 + n;

        // 3. 出力処理
        System.out.println(sum);
    }
}